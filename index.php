	  <div id="home-main-text" class="main-text">
		<p>The Coastwatch Station at Wells-next-the-Sea is one of 41 NCI Stations around 
		our coasts (seven more are in the process of being set up).</p>

		<p>Coastwatch at Wells started operating at Easter in 2006. Based in the old 
		coastguard lookout, one mile from Wells Quay at the end of the Beach Bank, 
		our watch-keepers maintain a visual watch on every day of the year.</p>

		<p>Overlooking Wells Beach and the outer harbour channel to the north, the 
		new ’Outer Harbour’ at Buxtons Bight and the inner harbour channel are 
		directly behind the lookout, with an easterly view to East Hills and 
		Sheringham Shoal and westerly across the The Wash to Skegness</p>
	
	  <p>Wells lookout is fairly unique in having a view over such a variety of 
		the coastal area and a working harbour. We have a busy harbour channel 
		with a fleet of 12 crab boats, 3 angling boats, a sailing school, a 
		sailing club, a water ski club and many pleasure craft. In addition the 
		service craft for the Sheringham Shoal Windfarm will be operating from 
		our new ’Outer Harbour’. To this end a channel deepening project has 
		now been completed to improve access to the harbour, this in turn makes 
		the channel more dangerous especially to holiday makers who are unfamiliar 
		with the fast flowing water. We keep a log of all vessels that enter and 
		leave Wells harbour channel and those that pass by further out to sea.</p>
		
		<p>During the summer months, particularly at the weekends, Wells beach can be 
		really busy, and most of our reportable incidents concern persons cut off 
		by an incoming tide having crossed the channel or walked out to East Hills 
		across the marsh. We try to encourage visitors to ask our advice if they are 
		planning to do something that might be potentially hazardous.</p>
		
		<p>Windsurfers and canoeists can easily get into trouble and are difficult to 
		spot from a distance, if you are planning to leave the harbour in a small 
		craft please let us know and we will keep an eye on you.</p>
		
		<p>Tidal information is displayed at the lookout and a siren is sounded 
		(by the Beach Supervisor) as the tide rises to warn you that you need 
		to cross back to the beach quickly. You are welcome to telephone the 
		lookout (see contact button on the left) for a weather report, but not 
		a forecast, you must ask the Coastguard at Yarmouth for this.</p>
		 
		<p>Come up to the lookout to see what we do, only two at a time please as 
		there’s not much room, keep your visit brief as we are often very busy. 
		Please consider supporting us as we are an entirely volunteer service 
		and rely on the generosity of the public to run the lookout.</p>
		
		<p>We currently have 47 dedicated watch-keepers who all believe that what 
		they do helps to make Wells harbour and beach a safer place for all who 
		use it.</p><br />
		
		<p><strong>Applications from potential volunteers are always welcome – 
		see ‘How to volunteer’ on the left</strong></p>
