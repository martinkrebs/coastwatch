      potential recruits, all we ask you to do is to commit to at least one three 
      hour watch per week. You must be in good health, have good eyesight and hearing.
      </p><br />
      
      <h2>What experience will you need?</h2>
      <p>You don’t need to have any maritime experience, although local knowledge 
      (as a dinghy sailor maybe) would be particularly useful. Coastwatch Wells 
      will provide full training in the skills you need to become a qualified 
      watch-keeper.</p><br /> 
      
      <h2>How to apply</h2>
      <p>Interested? If so click ‘Contact Coastwatch’ on the home page and send us you 
      details and we will post an application pack to you. Alternatively come up to 
      the lookout, see what we do, and collect an application pack for yourself.</p> 
      
      <p>When you have filled in the forms post them to the address given or return 
      them to the lookout. The Station Records Officer will then contact you to ask 
      you to come to the lookout as an observer on a full watch to see if you want 
      to go ahead with training.</p><br /> 
      
      <h2>Training</h2>
      <p>The next stage would be a three month probationary and training period mostly 
      in the lookout, but it will include some classroom sessions, these are normally 
      held in the Wells Library.</p> 
      
      <p>At the end of this time you would be tested and accredited as a qualified 
      watch-keeper and become part of the Coastwatch team at Wells.</p>			
