      <h2>Who are we?</h2>
      <p>The National Coastwatch Institution (NCI) is a voluntary organisation set up 
      in 1994 to restore a visual watch along UK shores after many small Coastguard 
      stations had to close down as part of government cut-backs.</p>
      
      <p>When two fishermen lost their lives off the Cornish coast below a recently 
      closed look-out, local people decided to restore the visual watch and the 
      first Coastwatch station opened at Bass Point, on the Lizard. NCI was born. 
      Others quickly followed suit and by 2007 thirty six operational stations were 
      keeping watch around the British Isles.</p> 
      
      <p>Sophisticated technology has vastly improved safety at sea but there is 
      no substitute for a watchful pair of eyes. Accidents do happen and a 
      computer cannot spot a distress flare, and overturned boat or a yachtsman 
      with engine failure.</p>
      
      <p>Each NCI station is manned by a team of fully trained volunteers who keep a 
      daylight watch 365 days a year. Stations are equipped with telescopes, radar, 
      telephone and weather instruments as well as up to date local sea charts. 
      Close contact with the Maritime Coastguard Agency (MCA) aims to promote 
      stations to Declared Facility Status in order to become part of the Search 
      and Rescue team.</p><br />
      
      <h2>Funding NCI</h2>
      <p>The NCI is controlled by a Board of Trustees with a constitution agreed by 
      the Charities Commission.Individual stations are run by a manager who is 
      responsible for the provision of trained personnel and maintenance of the 
      look-out. Wells-next-the-Sea is unique in that it is currently run by a 
      committee elected annually by the members of the team.</p>
      
      <p>Fund raising relies heavily upon the good will of the local people and the 
      efforts of the local watch-keepers and their friends with occasional assistance 
      from outside sources who identify with the aims of NCI and wish to keep the 
      charity open.Funding is required to maintain the building, provide up to date equipment and 
      paperwork to keep the station open. Watch-keepers currently provide their own 
      uniform and do not receive any expenses.</p><br />
      
      <h2>Some facts about Coastwatch</h2>
      <p>There are now forty one stations around the coast of the UK. There are 
        over 1,700 volunteers who agree to train and keep watch. Most stations 
        keep watch for over 2500 hours a year (Wells station was open for over 
        3350 hours last year). Over 1,200 incidents were reported nationally by 
        NCI in 2009 of these 49 resulted in RNLI rescues.			
      </p>
      
